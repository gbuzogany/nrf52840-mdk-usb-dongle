static void idle_state_handle(void)
{
    int val = nrf_gpio_pin_read(PIN_IN);
    if (val != last_read) {
        last_read = val;
        valve_closed = !val;
        NRF_LOG_INFO("changed");
    }
    update_motor();
    if (NRF_LOG_PROCESS() == false)
    {
        nrf_pwr_mgmt_run();
    }
}

